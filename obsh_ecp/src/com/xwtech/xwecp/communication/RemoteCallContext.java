package com.xwtech.xwecp.communication;
import org.apache.log4j.Logger;

public class RemoteCallContext
{
	private static final Logger	logger	= Logger.getLogger(RemoteCallContext.class);
	
	//用于地市路由
	protected String userCity;
	
	//用于传递调用渠道
	protected String invokeChannel;
	//放一个traceId
	protected String traceId;
	
	public String getTraceId() {
		return traceId;
	}

	public void setTraceId(String traceId) {
		this.traceId = traceId;
	}

	public String getUserCity()
	{
		return userCity;
	}

	public void setUserCity(String userCity)
	{
		this.userCity = userCity;
	}

	public String getInvokeChannel()
	{
		return invokeChannel;
	}

	public void setInvokeChannel(String invokeChannel)
	{
		this.invokeChannel = invokeChannel;
	}
}
