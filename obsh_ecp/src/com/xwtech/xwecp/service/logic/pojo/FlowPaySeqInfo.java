package com.xwtech.xwecp.service.logic.pojo;

/**
 * 流量账户收支流水明细
 * @author 张斌
 * 2015-4-22
 */
public class FlowPaySeqInfo
{
	private String inServNumber;//流入用户号码

	private String inUserName;//流入用户名称

	private String outServNumber;//流出用户号码

	private String outUserName;//流出用户名称
	
	private String inCome;//收支
	
	private String flowValue;//交易流量值KB.无小数

	private String dealDate;//交易时间YYYYMMDDHH24MISS

	private String reMark;//备注

	public String getInServNumber() {
		return inServNumber;
	}

	public void setInServNumber(String inServNumber) {
		this.inServNumber = inServNumber;
	}

	public String getInUserName() {
		return inUserName;
	}

	public void setInUserName(String inUserName) {
		this.inUserName = inUserName;
	}

	public String getOutServNumber() {
		return outServNumber;
	}

	public void setOutServNumber(String outServNumber) {
		this.outServNumber = outServNumber;
	}

	public String getOutUserName() {
		return outUserName;
	}

	public void setOutUserName(String outUserName) {
		this.outUserName = outUserName;
	}

	public String getInCome() {
		return inCome;
	}

	public void setInCome(String inCome) {
		this.inCome = inCome;
	}

	public String getFlowValue() {
		return flowValue;
	}

	public void setFlowValue(String flowValue) {
		this.flowValue = flowValue;
	}

	public String getDealDate() {
		return dealDate;
	}

	public void setDealDate(String dealDate) {
		this.dealDate = dealDate;
	}

	public String getReMark() {
		return reMark;
	}

	public void setReMark(String reMark) {
		this.reMark = reMark;
	}

}