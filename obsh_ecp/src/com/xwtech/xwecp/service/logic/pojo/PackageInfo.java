package com.xwtech.xwecp.service.logic.pojo;


public class PackageInfo
{
	private String pkgId;

	private String pkgState;

	private String pkgType;

	private String pkgName;

	private String pkgLevel;

	private String beginDate;

	private String endDate;

	private String feeDesc;

	public void setPkgId(String pkgId)
	{
		this.pkgId = pkgId;
	}

	public String getPkgId()
	{
		return this.pkgId;
	}

	public void setPkgState(String pkgState)
	{
		this.pkgState = pkgState;
	}

	public String getPkgState()
	{
		return this.pkgState;
	}

	public void setPkgType(String pkgType)
	{
		this.pkgType = pkgType;
	}

	public String getPkgType()
	{
		return this.pkgType;
	}

	public void setPkgName(String pkgName)
	{
		this.pkgName = pkgName;
	}

	public String getPkgName()
	{
		return this.pkgName;
	}

	public void setPkgLevel(String pkgLevel)
	{
		this.pkgLevel = pkgLevel;
	}

	public String getPkgLevel()
	{
		return this.pkgLevel;
	}

	public void setBeginDate(String beginDate)
	{
		this.beginDate = beginDate;
	}

	public String getBeginDate()
	{
		return this.beginDate;
	}

	public void setEndDate(String endDate)
	{
		this.endDate = endDate;
	}

	public String getEndDate()
	{
		return this.endDate;
	}

	public void setFeeDesc(String feeDesc)
	{
		this.feeDesc = feeDesc;
	}

	public String getFeeDesc()
	{
		return this.feeDesc;
	}

}