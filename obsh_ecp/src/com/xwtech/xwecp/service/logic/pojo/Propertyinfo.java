package com.xwtech.xwecp.service.logic.pojo;

/**
 * 订单提交子结点 - 有线业务属性子节点
 * @author 仰孝庆
 * 2014 -5 -29
 */
public class Propertyinfo {
	String itemid="";// 属性编码
	String itemvalue="";//  属性值
	public Propertyinfo() {
		super();
	}
	public Propertyinfo(String itemid, String itemvalue) {
		super();
		this.itemid = itemid;
		this.itemvalue = itemvalue;
	}
	public String getItemid() {
		return itemid;
	}
	public void setItemid(String itemid) {
		this.itemid = itemid;
	}
	public String getItemvalue() {
		return itemvalue;
	}
	public void setItemvalue(String itemvalue) {
		this.itemvalue = itemvalue;
	}
	
}
